package com.lec.ex;

public class Ex03 {

	public static void main(String[] args) {
		
		int n1 = 10;
		int n2 = 20;
		boolean result;
		
		result = n1 > n2;
		System.out.println("n1 > n2 = " + result);
		
		result = n1 < n2;
		System.out.println("n1 < n2 = " + result);
		
		result = n1 >= n2;
		System.out.println("n1 >= n2 = " + result);
		
		result = n1 <= n2;
		System.out.println("n1 <= n2 = " + result);
		
		result = n1 == n2;
		System.out.println("n1 == n2 = " + result);
		
		result = n1 != n2;
		System.out.println("n1 != n2 = " + result);
	}
}
